char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
			str[i] += 32;
		i++;
	}
	return (str);
}

char	*ft_strcapitalize(char *str)
{
	int	i;
	int	i1;

	i = 0;
	i1 = 1;
	ft_strlowcase(str);
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
		{
			if (i1 == 1)
				str[i] -= 32;
				i1 = 0;
		}
		else if (str[i] >= '0' && str[i] <= '9')
				i1 = 0;
		else
				i1 = 1;
		i++;
	}
	return (str);
}
